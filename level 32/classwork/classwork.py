def opposite(number):
    return -number

print(opposite(1))   
print(opposite(14))  
print(opposite(-34)) 
print(opposite(2.5)) 
print(opposite(-0.75)) 



def repeat_string(n, s):
    return s * n

print(repeat_string(6, "I"))        
print(repeat_string(5, "Hello"))  


